    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        int canCarl = 0;
        int canJaim = 0;
        int canJuan = 0;
        int canPepi = 0;
        
        int pCarl = 0;
        int pJaim = 0;
        int pJuan = 0;
        int pPepi = 0;
        
        for(int i=0; i<venta.length; i++)
        {
            if(venta[i][0].equalsIgnoreCase("Carlitos"))
                canCarl = canCarl + 1;
            else if(venta[i][0].equalsIgnoreCase("Jaimito"))
                canJaim = canJaim + 1;
            else if(venta[i][0].equalsIgnoreCase("Juanito"))
                canJuan = canJuan + 1;
            else if(venta[i][0].equalsIgnoreCase("Pepito"))
                canPepi = canPepi + 1;
        }
        
        lblCanCar.setText(String.valueOf(canCarl));
        pCarl = (int)((canCarl*100)/venta.length);
        prbCarl.setValue(pCarl);
        
        lblCanJai.setText(String.valueOf(canJaim));
        pJaim = (int)((canJaim*100)/venta.length);
        prbJaim.setValue(pJaim);
        
        lblCanJua.setText(String.valueOf(canJuan));
        pJuan = (int)((canJuan*100)/venta.length);
        prbJuan.setValue(pJuan);
        
        lblCanPep.setText(String.valueOf(canPepi));
        pPepi = (int)((canPepi*100)/venta.length);
        prbPepi.setValue(pPepi);
    }//GEN-LAST:event_formWindowOpened

