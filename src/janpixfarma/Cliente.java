    private void estadoCajas(boolean estado) {
        this.txtDNI.setEnabled(estado);
        this.txtApellidos.setEnabled(estado);
        this.txtNombres.setEnabled(estado);
        this.txtBonus.setEnabled(estado);

    }

    private void limpiar() {
        this.txtDNI.setText(null);
        this.txtApellidos.setText(null);
        this.txtNombres.setText(null);
        this.txtBonus.setText(null);

    }

    private void llenarGria(String item, String dni, String apellidos, String nombres, String bonus) {
        model.addRow(new Object[]{item, dni, apellidos, nombres, bonus});

    }

    private boolean BuscaCodigo() {
        boolean ExisteCodigo = false;
        for (int i = 0; i < Clientes.length; i++) {
            if (txtDNI.getText().toUpperCase() != null && txtDNI.getText().toUpperCase().equals(Clientes[i][1])) {
                ExisteCodigo = true;
                break;
            }
        }
        return ExisteCodigo;
    }
    
    public static int BuscaDNI(String dni) {
        int indice = -1;
        for (int i = 0; i < Clientes.length; i++) {
            if (dni.toUpperCase() != null && dni.toUpperCase().equals(Clientes[i][1])) {
                indice=i;
                break;
            }
        }
        return indice;
    }

