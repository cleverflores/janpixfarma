    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) tblReporte.getModel();
        while(modelo.getRowCount()>0)modelo.removeRow(0);
        String vendedor;
        vendedor = cmbVendedor.getSelectedItem().toString();
        
        if(vendedor.equalsIgnoreCase("Todos"))
        {
            for(int i=0; i<venta.length; i++)
            {
                modelo.addRow(new Object[]{venta[i][0],venta[i][1],venta[i][2],venta[i][3],venta[i][4],venta[i][5],venta[i][6]});
            }
        } else {
            for(int i=0; i<venta.length; i++)
            {
                if(vendedor.equalsIgnoreCase(venta[i][0]))
                    modelo.addRow(new Object[]{venta[i][0],venta[i][1],venta[i][2],venta[i][3],venta[i][4],venta[i][5],venta[i][6]});
            }
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

